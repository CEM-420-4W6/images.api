using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.DependencyInjection;
using images.api.Data;
var builder = WebApplication.CreateBuilder(args);
builder.Services.AddDbContext<imagesapiContext>(options =>
    options.UseSqlServer(builder.Configuration.GetConnectionString("imagesapiContext") ?? throw new InvalidOperationException("Connection string 'imagesapiContext' not found.")));

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddCors(options =>
{
    options.AddPolicy("AllowAll", builder =>
    {
        builder.AllowAnyHeader();
        builder.AllowAnyMethod();
        builder.AllowAnyOrigin();
    });
});

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseCors("AllowAll");

app.UseAuthorization();

app.MapControllers();

app.Run();
